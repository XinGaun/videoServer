package com.entity;
/**
 * 文案模块表实体类
 * @author vip
 *
 */
public class DocumentsTab {
	private Integer docu_id;
	private String docu_title;
	private String docu_status;
	public Integer getDocu_id() {
		return docu_id;
	}
	public void setDocu_id(Integer docu_id) {
		this.docu_id = docu_id;
	}
	public String getDocu_title() {
		return docu_title;
	}
	public void setDocu_title(String docu_title) {
		this.docu_title = docu_title;
	}
	public String getDocu_status() {
		return docu_status;
	}
	public void setDocu_status(String docu_status) {
		this.docu_status = docu_status;
	}
	
}
