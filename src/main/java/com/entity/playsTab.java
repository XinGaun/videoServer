package com.entity;

public class playsTab {
	int plays_id;
	int user_id;
	int video_id;
	String plays_time;
	String plays_date;
	public int getPlays_id() {
		return plays_id;
	}
	public void setPlays_id(int plays_id) {
		this.plays_id = plays_id;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public int getVideo_id() {
		return video_id;
	}
	public void setVideo_id(int video_id) {
		this.video_id = video_id;
	}
	public String getPlays_time() {
		return plays_time;
	}
	public void setPlays_time(String plays_time) {
		this.plays_time = plays_time;
	}
	public String getPlays_date() {
		return plays_date;
	}
	public void setPlays_date(String plays_date) {
		this.plays_date = plays_date;
	}
	
}
